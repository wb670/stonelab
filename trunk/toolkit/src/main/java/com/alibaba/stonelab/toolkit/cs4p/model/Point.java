package com.alibaba.stonelab.toolkit.cs4p.model;

/**
 * 地图上的点
 * 
 * @author Stone.J 2010-9-17 下午01:13:20
 */
public class Point {

    private long x; // x坐标
    private long y; // y坐标

    public Point(long x, long y){
        this.x = x;
        this.y = y;
    }

    @Override
    public String toString() {
        return "Point [x=" + x + ", y=" + y + "]";
    }

    public long getX() {
        return x;
    }

    public void setX(long x) {
        this.x = x;
    }

    public long getY() {
        return y;
    }

    public void setY(long y) {
        this.y = y;
    }

}
